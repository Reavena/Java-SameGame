package gridgames;

/**
 * Enumeration type which helps to represent different levels of difficulty to
 * be used in game implementations.
 */
public enum Difficulty {
    /** The easiest level of difficulty, ideal for newcomers. */
    EASY,
    /** A balanced level of difficulty. */
    MEDIUM,
    /** The highest level of difficulty, for hardcore players. */
    HARD;
}
